    function ClickMenu() {
        $("div.pagevk").css("display", "none");        
        var id = $(this).attr("data-id");
        setMenuHtml(id);        
        $(".dropdown-menu").css("display", "none");
        $("#collapsibleNavbar").removeClass("show");
    }
    function GetHtml(list, menuId) {
        for (var i = 0; i < list.length; i++) {
            if (list[i].Id == menuId) {
                return list[i].Html;
            }
        }
    }

